import java.util.*;

public class Input {
    public static void main (String args[]){
        Scanner sc = new Scanner(System.in);
        String input = sc.next();
        System.out.println(input);

        String name = sc.nextLine();
        System.out.print(name);

        int number = sc.nextInt();
        System.out.print(number);

    }
}


