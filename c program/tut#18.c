// tut#18 (typecasting)

// convert one data type to other data type

#include <stdio.h>
int main ()
{
    int a = 3;
    float b = 56;
    float c = (float)54/5;
    printf("the value of a is %d\n",a);
    printf("the value of b is %d\n",(int)b);
    printf("the value of c is %f\n",c);
  
    return 0;
}